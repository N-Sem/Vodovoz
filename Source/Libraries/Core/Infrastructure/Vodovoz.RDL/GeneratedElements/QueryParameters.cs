using System;
using System.Xml.Serialization;
using System.Xml;
using System.Linq;
using System.Collections.Generic;

namespace Vodovoz.RDL.Elements
{
	[Serializable()]
	[XmlType()]
	public partial class QueryParameters
	{
		private List<QueryParameter> queryParameterField = new List<QueryParameter>();
		private List<XmlAttribute> anyAttrField = new List<XmlAttribute>();

		[XmlElement("QueryParameter")]
		public List<QueryParameter> QueryParameter
		{
			get => queryParameterField;
			set => queryParameterField = value;
		}

		[XmlIgnore()]
		public List<XmlAttribute> AnyAttrList
		{
			get => anyAttrField;
			set => anyAttrField = value;
		}

		[XmlAnyAttribute()]
		public XmlAttribute[] AnyAttr
		{
			get => AnyAttrList.ToArray();
			set => AnyAttrList = value == null ? new List<XmlAttribute>() : value.ToList();
		}
	}
}
