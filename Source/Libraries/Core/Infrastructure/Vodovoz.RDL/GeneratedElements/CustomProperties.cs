using System;
using System.Xml.Serialization;
using System.Xml;
using System.Linq;
using System.Collections.Generic;

namespace Vodovoz.RDL.Elements
{
	[Serializable()]
	[XmlType()]
	public partial class CustomProperties
	{
		private List<CustomProperty> customPropertyField = new List<CustomProperty>();
		private List<XmlAttribute> anyAttrField = new List<XmlAttribute>();

		[XmlElement("CustomProperty")]
		public List<CustomProperty> CustomProperty
		{
			get => customPropertyField;
			set => customPropertyField = value;
		}

		[XmlIgnore()]
		public List<XmlAttribute> AnyAttrList
		{
			get => anyAttrField;
			set => anyAttrField = value;
		}

		[XmlAnyAttribute()]
		public XmlAttribute[] AnyAttr
		{
			get => AnyAttrList.ToArray();
			set => AnyAttrList = value == null ? new List<XmlAttribute>() : value.ToList();
		}
	}
}
